public class Tbl_pais {
    private Integer tbl_pais_id;
    private String tbl_pais_iso;
    private String tbl_pais_nombre;
    private Integer tbl_pais_estado;

    public Integer getTbl_pais_id() {
        return tbl_pais_id;
    }

    public void setTbl_pais_id(Integer tbl_pais_id) {
        this.tbl_pais_id = tbl_pais_id;
    }

    public String getTbl_pais_iso() {
        return tbl_pais_iso;
    }

    public void setTbl_pais_iso(String tbl_pais_iso) {
        this.tbl_pais_iso = tbl_pais_iso;
    }

    public String getTbl_pais_nombre() {
        return tbl_pais_nombre;
    }

    public void setTbl_pais_nombre(String tbl_pais_nombre) {
        this.tbl_pais_nombre = tbl_pais_nombre;
    }

    public Integer getTbl_pais_estado() {
        return tbl_pais_estado;
    }

    public void setTbl_pais_estado(Integer tbl_pais_estado) {
        this.tbl_pais_estado = tbl_pais_estado;
    }
}
